// Locale management
window.MANDARLY_LOCALE = (function () {
  const saved = localStorage.getItem('mandarly_locale');
  if (saved) return saved;
  const browser = (navigator.language || 'en').toLowerCase();
  return browser.startsWith('zh') ? 'zh-CN' : 'en';
})();

window.MANDARLY_T = function (key) {
  const dict = window.MANDARLY_I18N[window.MANDARLY_LOCALE] || window.MANDARLY_I18N['en'];
  return dict[key] || key;
};

window.MANDARLY_APPLY_I18N = function () {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    el.innerHTML = window.MANDARLY_T(key);
  });
  document.documentElement.lang = window.MANDARLY_LOCALE;
};

window.MANDARLY_TOGGLE_LOCALE = function () {
  window.MANDARLY_LOCALE = window.MANDARLY_LOCALE === 'zh-CN' ? 'en' : 'zh-CN';
  localStorage.setItem('mandarly_locale', window.MANDARLY_LOCALE);
  window.MANDARLY_APPLY_I18N();
  document.dispatchEvent(new CustomEvent('mandarly:locale-changed'));
};
